# Abstraction:
## Ce concept consiste à faire montrer que les attributs qui sont nécéssaires et d'effacer ou de ne pas montrer les attributs ou methodes non nécéssaire.
## Par exemple dans une méthode qui permet d'envoyer un mail, si on divise notre méthode, on a pas besoin de montrer les méthodes qui permettent de se connecter au serveur, ou de preparer le corps de l'email et l'envoie elle meme
## Il suffit juste de transformer les fonctions en privé en ajoutant __ au nom comme pour les attributs privés


# Polymorphisme:
